public class KoreaDisclosure {
    private String   dateTime;
    private String companyName;
    private String headline;
    private String savingUrl;
    private String rnsId;

    public String getDateTime() {
        return dateTime;
    }

    public void setDateTime(String dateTime) {
        this.dateTime = dateTime;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getHeadline() {
        return headline;
    }

    public void setHeadline(String headline) {
        this.headline = headline;
    }

    public String getSavingUrl() {
        return savingUrl;
    }

    public void setSavingUrl(String savingUrl) {
        this.savingUrl = savingUrl;
    }

    public String getRnsId() {
        return rnsId;
    }

    public void setRnsId(String rnsId) {
        this.rnsId = rnsId;
    }
}
